<p>
<b>Super Market Transactions</b>
A supermarket is a self-service shop offering a wide variety of food, beverages and household products, organized into sections and shelves. It is larger and has a wider selection than earlier grocery stores, but is smaller and more limited in the range of merchandise than a hypermarket or big-box market.
</p>
<p>
The supermarket typically has aisles for meat, fresh produce, dairy, and baked goods. Shelf space is also reserved for canned and packaged goods and for various non-food items such as kitchenware, household cleaners, pharmacy products and pet supplies. Some supermarkets also sell other household products that are consumed regularly, such as alcohol (where permitted), medicine, and clothes, and some sell a much wider range of non-food products: DVDs, sporting equipment, board games, and seasonal items (e.g., Christmas wrapping paper in December).
</p>
<p>
A larger full-service supermarket combined with a department store is sometimes known as a hypermarket. Other services may include those of banks, caf√©s, childcare centres/creches, insurance (and other financial services), Mobile Phone services, photo processing, video rentals, pharmacies or petrol stations. If the eatery in a supermarket is substantial enough, the facility may be called a "grocerant", a blend of "grocery" and "restaurant".
</p>
<p>
  The traditional supermarket occupies a large amount of floor space, usually on a single level. It is usually situated near a residential area in order to be convenient to consumers. The basic appeal is the availability of a broad selection of goods under a single roof, at relatively low prices. Other advantages include ease of parking and frequently the convenience of shopping hours that extend into the evening or even 24 hours of the day. Supermarkets usually allocate large budgets to advertising, typically through newspapers. They also present elaborate in-shop displays of products.
</p>
<p>
  Supermarkets typically are chain stores, supplied by the distribution centers of their parent companies thus increasing opportunities for economies of scale. Supermarkets usually offer products at relatively low prices by using their buying power to buy goods from manufacturers at lower prices than smaller stores can. They also minimise financing costs by paying for goods at least 30 days after receipt and some extract credit terms of 90 days or more from vendors. Certain products (typically staple foods such as bread, milk and sugar) are very occasionally sold as loss leaders so as to attract shoppers to their store. Supermarkets make up for their low margins by a high volume of sales, and with of higher-margin items bought by the attracted shoppers. Self-service with shopping carts (trolleys) or baskets reduces labor cost, and many supermarket chains are attempting further reduction by shifting to self-service check-out.
</p>
<p>
  In the early days of retailing, products generally were fetched by an assistant from shelves behind the merchant's counter while customers waited in front of the counter and indicated the items they wanted. Most foods and merchandise did not come in individually wrapped consumer-sized packages, so an assistant had to measure out and wrap the precise amount desired by the consumer. This offered opportunities for social interaction: many regarded this style of shopping as "a social occasion" and would often "pause for conversations with the staff or other customers".[2] These practices were by nature slow and labor-intensive and therefore also quite expensive. The number of customers who could be attended to at one time was limited by the number of staff employed in the store. Shopping for groceries also often involved trips to multiple specialty shops, such as a greengrocer, butcher, bakery, fishmonger and dry goods store; in addition to a general store. Milk and other items of short shelf life were delivered by a milkman.
</p>
<p>
  The concept of an inexpensive food market relying on large economies of scale was developed by Vincent Astor. He founded the Astor Market in 1915, investing $750,000 ($18 million in 2015 currency) of his fortune into a 165' by 125' (50x38 metre) corner of 95th and Broadway, Manhattan, creating, in effect, an open-air mini-mall that sold meat, fruit, produce and flowers.[3] The expectation was that customers would come from great distances ("miles around"), but in the end, even attracting people from ten blocks away was difficult, and the market folded in 1917.
</p>
    
